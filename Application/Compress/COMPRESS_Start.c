/*********************************************************************
*
*       _puts()
*
*  Function description
*    Local (static) replacement for puts.
*
*  Parameters
*    s: String to output.
*/
static void _puts(const char* s) {
#if (USE_EMBOS_VIEW != 0)
  //
  // Prevent using OS_SendString() from an interrupt as this is not
  // valid. Might happen in case we are overrun by packets.
  // OS_InInterrupt() might not be supported by ALL embOS ports but
  // should be support by typically any newer embOS port.
  //
  if (OS_InInterrupt() == 0) {
    OS_SendString(s);
  }
#elif (USE_RTT != 0)
  SEGGER_RTT_printf(0, s);
#else
  char c;

  for (;;) {
    c = *s++;
    if (c == 0) {
      break;
    }
#if USE_DCC
    JLINKDCC_SendChar(c);
#else
    (void)putchar(c);
#endif
  }
#endif
}


/*********************************************************************
*
*       _HandleData()
*
*  Function description
*    Called by the decompressor to deliver decompressed data.
*
*  Parameters
*    pUserContext - Context (not used here).
*    pData        - Pointer to decompressed data.
*    NumBytesData - Length of decompressed data in bytes.
*/
static int _HandleData(void *pUserContext, void *pData, unsigned NumBytesData) {
  char     Buff[2];
  char     *p;
  (void)pUserContext;

  //
  // Output decompressed data to debug console.
  //
  p = (char *)pData;
  while (NumBytesData-- > 0) {
    Buff[0] = *p++;
    Buff[1] = 0;
    _puts(Buff);
  }
  return 0;
}

